package org.westford.compositor.dispmanx.egl;

import dagger.Module;
import dagger.Provides;
import org.westford.compositor.core.RenderPlatform;
import org.westford.compositor.dispmanx.DispmanxPlatform;
import org.westford.compositor.dispmanx.DispmanxPlatformFactory;

import javax.annotation.Nonnull;
import javax.inject.Singleton;

import static org.westford.nativ.libbcm_host.Libbcm_host.DISPMANX_ID_HDMI;

@Module
public class DispmanxEglPlatformModule {

    @Provides
    @Singleton
    DispmanxPlatform createDispmanxPlatform(final DispmanxPlatformFactory dispmanxPlatformFactory) {
        //FIXME from config
        return dispmanxPlatformFactory.create(DISPMANX_ID_HDMI);
    }

    @Provides
    @Singleton
    RenderPlatform createPlatform(@Nonnull final DispmanxEglPlatformFactory dispmanxEglPlatformFactory) {
        return dispmanxEglPlatformFactory.create();
    }
}
